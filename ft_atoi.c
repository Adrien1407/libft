#include "libft.h"

int	ft_atoi(const char *str)
{
	size_t	i;
	int		negative;
	int		atoi;

	i = 0;
	atoi = 0;
	negative = 1;
	while ((str[i] >= 9 && str[i] <= 13) || (str[i] == 32))
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			negative = -negative;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		atoi = atoi * 10 + str[i] - '0';
		i++;
	}
	atoi = atoi * negative;
	return (atoi);
}
